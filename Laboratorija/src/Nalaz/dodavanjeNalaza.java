    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    
        try {
        NalazKontroler nalazKontroler = new NalazKontroler();
        Pacijent_Kontroler pacijentKontroler = new Pacijent_Kontroler();

        String naziv = nazivNalaza.getText().trim();
        String datum = jTextField2.getText().trim();
        String pacijentIdText = jTextField1.getText().trim();

        if (naziv.isEmpty() || datum.isEmpty() || pacijentIdText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite vrijednost u sva polja jer inace program ne moze raditi.");
            return;
        }

        int idPacijenta = Integer.parseInt(pacijentIdText);

        Pacijent pacijent = pacijentKontroler.nadjiPacijentaPoId(idPacijenta);
        if (pacijent == null) {
            JOptionPane.showMessageDialog(this, "Ne mozemo pronaci tog pacijenta.");
            return;
        }

        Nalaz nalaz = new Nalaz(naziv, datum, idPacijenta);

        int noviId = nalazKontroler.generisiIdNalaza();
        nalaz.setIdNalaza(noviId);

        nalazKontroler.dodajNalaz(nalaz);

        JOptionPane.showMessageDialog(this, "Nalaz uspje≈°no dodat!");

        nazivNalaza.setText("");
        jTextField2.setText("");
        jTextField1.setText("");
        
        uvezivanje uvezivanjeProzor = new uvezivanje(noviId);
        this.getDesktopPane().add(uvezivanjeProzor);
        uvezivanjeProzor.setVisible(true);


    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "ID je broj.");
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Greska: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton1ActionPerformed
