    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Molimo vas da odaberete nalaz iz tabele.");
    return;
    }

    int nalazId = (int) jTable1.getValueAt(selectedRow, 0);

    try {
        java.sql.Connection conn = new NalazKontroler().getKon();
        String sql = "SELECT p.naziv, np.IzmjereneVrijednosti "
                   + "FROM Nalaz_Pretraga np "
                   + "INNER JOIN Pretraga p ON np.Pretraga_ID = p.naziv "
                   + "WHERE np.Nalaza_ID = ?";
        java.sql.PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, nalazId);

        java.sql.ResultSet rs = ps.executeQuery();
        StringBuilder poruka = new StringBuilder();

        while (rs.next()) {
            String naziv = rs.getString("naziv");
            double izmjerena = rs.getDouble("IzmjereneVrijednosti");
            poruka.append("- ").append(naziv).append(": ").append(izmjerena).append("\n");
        }

        if (poruka.length() == 0) {
            poruka.append("Nema pretraga za odabrani nalaz.");
        }

        javax.swing.JOptionPane.showMessageDialog(this, poruka.toString(), "Pretrage nalaza", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        rs.close();
        ps.close();
        conn.close();

    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Gre≈°ka pri dohvatanju pretraga: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed


