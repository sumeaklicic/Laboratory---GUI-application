    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
    int red = jTable1.getSelectedRow();
    if (red == -1) {
        JOptionPane.showMessageDialog(this, "Molimo vas da odaberete red za brisanje.");
        return;
    }

    int potvrda = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da želite obrisati?", "Potvrda brisanja", JOptionPane.YES_NO_OPTION);
    if (potvrda != JOptionPane.YES_OPTION) {
        return;
    }

    int idVeze = (int) jTable1.getValueAt(red, 0);
    NalazPretraga_Kontroler kontroler = new NalazPretraga_Kontroler();
    boolean uspjeh = kontroler.obrisiNalazPretraga(idVeze);

    if (uspjeh) {
        JOptionPane.showMessageDialog(this, "Zapis uspješno obrisan.");
        ucitajTabelu(); 
    } else {
        JOptionPane.showMessageDialog(this, "Greška prilikom brisanja zapisa.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed


