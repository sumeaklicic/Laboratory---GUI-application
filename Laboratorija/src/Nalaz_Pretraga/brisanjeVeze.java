private void ucitajTabelu() {
    NalazPretraga_Kontroler kontroler = new NalazPretraga_Kontroler();
    List<OsnovneKlase.NalazPretraga> lista = kontroler.getSveNalazPretrage();

    String[] kolone = {"ID_VEZE", "Nalaza_ID", "Pretraga_ID", "IzmjereneVrijednosti"};
    Object[][] podaci = new Object[lista.size()][4];

    for (int i = 0; i < lista.size(); i++) {
        OsnovneKlase.NalazPretraga np = lista.get(i);
        podaci[i][0] = np.getId();
        podaci[i][1] = np.getNalazId();
        podaci[i][2] = np.getPretragaId();
        podaci[i][3] = np.getIzmjereneVrijednosti();
    }

    jTable1.setModel(new javax.swing.table.DefaultTableModel(podaci, kolone));
}



