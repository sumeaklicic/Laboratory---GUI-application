        private void ucitajPretrage() {
        Pretraga_Kontroler kontroler = new Pretraga_Kontroler();
        List<Pretraga> pretrage = null;
        try {
            pretrage = kontroler.svePretrage();
        } catch (SQLException ex) {
            System.out.println("Greska "+ ex.getMessage());
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); 

        for (Pretraga p : pretrage) {
            model.addRow(new Object[] { p.getNaziv(), p.getRefVrijednosti() });
        }
    }

