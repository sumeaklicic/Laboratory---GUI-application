    private void dodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dodajActionPerformed

        int[] odabraniRedovi = jTable1.getSelectedRows();

        if (odabraniRedovi.length == 0 || odabraniRedovi.length > 3) {
            JOptionPane.showMessageDialog(this, "Odaberite minimalno 1 a maksimalno 3 pretrage.");
            return;
        }

        String idNalazaText = idNalaza.getText().trim();
        if (idNalazaText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite ID nalaza.");
            return;
        }

        int idNalaza;
        
        try {
            idNalaza = Integer.parseInt(idNalazaText);
            database.NalazKontroler nalazKontroler = new database.NalazKontroler();
        boolean postoji = false;
        
        try {
        postoji = nalazKontroler.postojiNalaz(idNalaza);
        } catch (SQLException ex) {
        Logger.getLogger(uvezivanje.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Greška pri provjeri nalaza: " + ex.getMessage());
        return;
        }

        if (!postoji) {
            JOptionPane.showMessageDialog(this, "Nalaz sa unesenim ID-jem ne postoji u bazi.");
            return;
        }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID nalaza mora biti broj.");
            return;
        }

        String[] vrijednosti = new String[] {
            refVr1.getText().trim(),
            refVr2.getText().trim(),
            refvr3.getText().trim()
        };

        NalazPretraga_Kontroler kontroler = new NalazPretraga_Kontroler();

        try {
            for (int i = 0; i < odabraniRedovi.length; i++) {
                String nazivPretrage = jTable1.getValueAt(odabraniRedovi[i], 0).toString();
                String izmjerenaVrijednost = vrijednosti[i];

                if (izmjerenaVrijednost.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Unesite sve izmjerene vrijednosti.");
                    return;
                }

                kontroler.dodajNalazPretraga(idNalaza, nazivPretrage, izmjerenaVrijednost);
            }

            JOptionPane.showMessageDialog(this, "Uspješno dodano u bazu.");

            refVr1.setText("");
            refVr2.setText("");
            refvr3.setText("");
            jTable1.clearSelection();

            }catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greška pri unosu u bazu: " + ex.getMessage());
            }

    }//GEN-LAST:event_dodajActionPerformed


